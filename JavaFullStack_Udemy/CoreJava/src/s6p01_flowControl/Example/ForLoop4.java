package s6p01_flowControl.Example;

public class ForLoop4 {
	public static void main(String[] args) {
		
	   for (int i =1; i<=10; i++) {
		   System.out.println(i);
	   }
	   
	   //reverse
	   for (int j =10; j>=1; j--) {
		   System.out.println(j);
	   }
	   
	   /*
	   //infinite loop
	   for (; ;) {
		   System.out.println("test");
	   }*/
	   
	}
}
